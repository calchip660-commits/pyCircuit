int main(int argc, char **argv) {
  llvm::InitLLVM y(argc, argv);
  llvm::cl::ParseCommandLineOptions(argc, argv, "pycc\n");
  using Clock = std::chrono::steady_clock;
  const auto tProgramStart = Clock::now();
  uint64_t parseMs = 0;
  uint64_t passMs = 0;
  uint64_t emitMs = 0;

  const bool hasDirectCpp = !directCppOut.empty();
  const bool hasDirectVerilog = !directVerilogOut.empty();
  if (hasDirectCpp && hasDirectVerilog) {
    llvm::errs() << "error: `-cpp` and `-verilog` are mutually exclusive\n";
    return 1;
  }
  if (hasDirectCpp) {
    emitKind = "cpp";
    outputFilename = std::string(directCppOut);
  }
  if (hasDirectVerilog) {
    emitKind = "verilog";
    outputFilename = std::string(directVerilogOut);
  }
  if ((hasDirectCpp || hasDirectVerilog) && !outDir.empty()) {
    llvm::errs() << "error: direct output mode (-cpp/-verilog) cannot be combined with --out-dir\n";
    return 1;
  }
  if ((hasDirectCpp || hasDirectVerilog) && outputFilename.empty()) {
    llvm::errs() << "error: direct output mode requires a non-empty output path\n";
    return 1;
  }

  std::string buildProfileNorm = llvm::StringRef(buildProfile).lower();
  if (buildProfileNorm != "release" && buildProfileNorm != "dev-fast") {
    llvm::errs() << "error: unknown --build-profile: " << buildProfile
                 << " (expected: release|dev-fast)\n";
    return 1;
  }
  const bool isDevFastProfile = (buildProfileNorm == "dev-fast");

  // Dev-fast defaults to smaller C++ shards unless explicitly overridden.
  if (isDevFastProfile && cppShardThresholdLines.getNumOccurrences() == 0)
    cppShardThresholdLines = 16000;
  if (isDevFastProfile && cppShardThresholdBytes.getNumOccurrences() == 0)
    cppShardThresholdBytes = 768 * 1024;
  if (isDevFastProfile && cppShardMaxAstNodes.getNumOccurrences() == 0)
    cppShardMaxAstNodes = 192;

  std::string profileJsonPathResolved = profileJsonPath;
  if (profileJsonPathResolved.empty() && profilePassTiming) {
    if (!outDir.empty()) {
      llvm::SmallString<256> p(outDir);
      llvm::sys::path::append(p, "pycc_profile.json");
      profileJsonPathResolved = p.str().str();
    } else if (outputFilename != "-") {
      profileJsonPathResolved = (llvm::Twine(outputFilename) + ".profile.json").str();
    }
  }
  const bool collectPassTiming = profilePassTiming || !profileJsonPathResolved.empty();

  DialectRegistry registry;
  registry.insert<pyc::PYCDialect, mlir::arith::ArithDialect, mlir::func::FuncDialect, mlir::scf::SCFDialect>();
  mlir::func::registerInlinerExtension(registry);

  MLIRContext ctx(registry);
#ifdef _WIN32
  // MSYS2/MinGW builds can hit non-deterministic crashes when running nested
  // pass pipelines with multithreading enabled. Bring-up prefers robustness.
  ctx.disableMultithreading();
#endif
  ctx.loadAllAvailableDialects();

  const auto tParseStart = Clock::now();
  llvm::SourceMgr sm;
  auto fileOrErr = llvm::MemoryBuffer::getFileOrSTDIN(inputFilename);
  if (!fileOrErr) {
    llvm::errs() << "error: cannot read " << inputFilename << "\n";
    return 1;
  }
  sm.AddNewSourceBuffer(std::move(*fileOrErr), llvm::SMLoc());

  OwningOpRef<ModuleOp> module = parseSourceFile<ModuleOp>(sm, &ctx);
  if (!module) {
    llvm::errs() << "error: failed to parse MLIR\n";
    return 1;
  }
  parseMs = static_cast<uint64_t>(std::chrono::duration_cast<std::chrono::milliseconds>(Clock::now() - tParseStart).count());
  uint64_t moduleOpCount = countModuleOps(*module);

  // Host-side testbench payload modules are emitted directly without hardware
  // lowering passes.
  if (getTbPayloadAttr(*module).has_value()) {
    if (!outDir.empty()) {
      llvm::errs() << "error: testbench payload mode does not support --out-dir; use direct -cpp/-verilog or -o\n";
      return 1;
    }
    std::error_code ec;
    llvm::raw_fd_ostream os(outputFilename, ec, llvm::sys::fs::OF_Text);
    if (ec) {
      llvm::errs() << "error: cannot open " << outputFilename << ": " << ec.message() << "\n";
      return 1;
    }
    if (failed(emitTbTextFromPayload(*module, emitKind, os)))
      return 1;
    return 0;
  }

  bool cppOnly = false;
  if (simMode == "default") {
    cppOnly = false;
  } else if (simMode == "cpp-only") {
    cppOnly = true;
  } else {
    llvm::errs() << "error: unknown --sim-mode: " << simMode << " (expected: default|cpp-only)\n";
    return 1;
  }

  if (emitStructuralMode != "auto" && emitStructuralMode != "on" && emitStructuralMode != "off") {
    llvm::errs() << "error: unknown --emit-structural: " << emitStructuralMode << " (expected: auto|on|off)\n";
    return 1;
  }
  if (emitStructuralMode == "on" || emitStructuralMode == "off") {
    bool enableStructural = (emitStructuralMode == "on");
    for (func::FuncOp f : module->getOps<func::FuncOp>()) {
      if (enableStructural)
        f->setAttr("pyc.emit.structural", StringAttr::get(&ctx, "true"));
      else
        f->removeAttr("pyc.emit.structural");
    }
  }

  bool strictHierarchy = false;
  bool instantiateHierarchy = false;
  if (hierarchyPolicy == "strict") {
    strictHierarchy = true;
  } else if (hierarchyPolicy == "instantiate") {
    instantiateHierarchy = true;
  } else {
    llvm::errs() << "error: unknown --hierarchy-policy: " << hierarchyPolicy
                 << " (expected: strict | instantiate)\n";
    return 1;
  }

  const std::set<std::string> moduleSymbolsBefore = collectFrontendModuleSymbols(*module);

  InlinePolicyDecision inlineDecision;
  inlineDecision.mode = inlinePolicy;
  bool forceNoInline = noInline;
  if (forceNoInline) {
    inlineDecision.enableInline = false;
    inlineDecision.reason = "cli --noinline";
  } else if (inlinePolicy == "default") {
    if (strictHierarchy || instantiateHierarchy) {
      inlineDecision.enableInline = false;
      inlineDecision.reason = strictHierarchy ? "strict hierarchy default"
                                              : "instantiate hierarchy default";
    } else if (isDevFastProfile) {
      inlineDecision.enableInline = false;
      inlineDecision.reason = "dev-fast default";
    } else {
      inlineDecision.enableInline = true;
      inlineDecision.reason = "release default";
    }
  } else if (inlinePolicy == "off") {
    inlineDecision.enableInline = false;
    inlineDecision.reason = "inline-policy=off";
  } else if (llvm::StringRef(inlinePolicy).starts_with("threshold:")) {
    llvm::StringRef n = llvm::StringRef(inlinePolicy).drop_front(strlen("threshold:"));
    uint64_t threshold = 0;
    if (n.getAsInteger(10, threshold) || threshold == 0) {
      llvm::errs() << "error: invalid --inline-policy threshold, expected threshold:<positive-int>\n";
      return 1;
    }
    inlineDecision.mode = "threshold";
    inlineDecision.thresholdOps = threshold;
    inlineDecision.enableInline = moduleOpCount <= threshold;
    inlineDecision.reason = inlineDecision.enableInline ? "module-op-count <= threshold" : "module-op-count > threshold";
  } else {
    llvm::errs() << "error: unknown --inline-policy: " << inlinePolicy
                 << " (expected: default|off|threshold:<n>)\n";
    return 1;
  }

  // Both strict and instantiate hierarchy prevent the MLIR inliner from
  // collapsing module boundaries.
  if ((strictHierarchy || instantiateHierarchy) && inlineDecision.enableInline) {
    llvm::errs() << "error: " << hierarchyPolicy.getValue()
                 << " hierarchy requires non-inline module compilation\n";
    llvm::errs() << "  inline_policy=" << inlineDecision.mode
                 << " enables inliner (reason: " << inlineDecision.reason << ")\n";
    llvm::errs() << "  hint: use --inline-policy=off (or --inline-policy=default with --hierarchy-policy="
                 << hierarchyPolicy.getValue() << ")\n";
    return 1;
  }

  unsigned effectiveCanonicalizeBudget = canonicalizeBudget;
  if (effectiveCanonicalizeBudget == 0 && isDevFastProfile)
    effectiveCanonicalizeBudget = 6;

  GreedyRewriteConfig canonicalizeCfg;
  if (effectiveCanonicalizeBudget > 0) {
    canonicalizeCfg.setMaxIterations(static_cast<int64_t>(effectiveCanonicalizeBudget));
    canonicalizeCfg.setMaxNumRewrites(static_cast<int64_t>(effectiveCanonicalizeBudget) * 4096);
  }

  // Cleanup + optimization pipeline tuned for netlist-style emission.
  PassManager pm(&ctx);
  std::unique_ptr<PassTimingCollector> passTimingStorage;
  PassTimingCollector *passTimingCollector = nullptr;
  if (collectPassTiming) {
    passTimingStorage = std::make_unique<PassTimingCollector>();
    passTimingCollector = passTimingStorage.get();
    pm.addInstrumentation(std::move(passTimingStorage));
  }
  pm.addPass(pyc::createCheckFrontendContractPass());
  pm.addPass(pyc::createInlineFunctionsPass());
  pm.addPass(pyc::createCheckHierarchyDisciplinePass());
  pm.addPass(createSymbolDCEPass());
  if (inlineDecision.enableInline)
    pm.addPass(createInlinerPass());
  pm.addPass(createCanonicalizerPass(canonicalizeCfg));
  pm.addPass(createCSEPass());
  pm.addPass(createSCCPPass());
  addRemoveDeadValuesPassIfSupported(pm);
  pm.addNestedPass<func::FuncOp>(pyc::createEliminateDeadInstancesPass());
  pm.addPass(createSymbolDCEPass());

  pm.addNestedPass<func::FuncOp>(pyc::createLowerSCFToPYCStaticPass());
  pm.addNestedPass<func::FuncOp>(pyc::createEliminateWiresPass());
  pm.addNestedPass<func::FuncOp>(pyc::createEliminateDeadStatePass());
  pm.addNestedPass<func::FuncOp>(pyc::createCombCanonicalizePass());
  pm.addNestedPass<func::FuncOp>(pyc::createSLPPackWiresPass());
  pm.addPass(pyc::createCheckCombCyclesPass());
  pm.addPass(pyc::createCheckClockDomainsPass());
  pm.addNestedPass<func::FuncOp>(pyc::createPackI1RegsPass());
  const bool enableFuseComb = (!cppOnly) || !cppOnlyPreserveOps;
  if (enableFuseComb)
    pm.addNestedPass<func::FuncOp>(pyc::createFuseCombPass());
  pm.addPass(createCanonicalizerPass(canonicalizeCfg));
  pm.addPass(createCSEPass());
  addRemoveDeadValuesPassIfSupported(pm);
  pm.addNestedPass<func::FuncOp>(pyc::createEliminateDeadInstancesPass());
  pm.addPass(createSymbolDCEPass());
  pm.addNestedPass<func::FuncOp>(pyc::createCheckFlatTypesPass());
  pm.addNestedPass<func::FuncOp>(pyc::createCheckNoDynamicPass());
  pm.addPass(pyc::createCheckLogicDepthPass(logicDepthLimit));
  pm.addNestedPass<func::FuncOp>(pyc::createCollectCompileStatsPass());
  const auto tPassStart = Clock::now();
  if (failed(pm.run(*module))) {
    llvm::errs() << "error: pass pipeline failed\n";
    return 1;
  }
  passMs = static_cast<uint64_t>(std::chrono::duration_cast<std::chrono::milliseconds>(Clock::now() - tPassStart).count());

  const std::set<std::string> moduleSymbolsAfter = collectFrontendModuleSymbols(*module);
  const bool hierarchyPreserved = (moduleSymbolsBefore == moduleSymbolsAfter);
  if (strictHierarchy && !hierarchyPreserved) {
    llvm::errs() << "error: strict hierarchy policy violation: frontend module symbol set changed after passes\n";
    llvm::errs() << "  before_count=" << moduleSymbolsBefore.size()
                 << " after_count=" << moduleSymbolsAfter.size() << "\n";
    for (const auto &sym : moduleSymbolsBefore) {
      if (!moduleSymbolsAfter.count(sym))
        llvm::errs() << "  removed_module=" << sym << "\n";
    }
    for (const auto &sym : moduleSymbolsAfter) {
      if (!moduleSymbolsBefore.count(sym))
        llvm::errs() << "  added_module=" << sym << "\n";
    }
    return 1;
  }
  // instantiate mode: warn (but don't fail) if modules were removed
  if (instantiateHierarchy && !hierarchyPreserved) {
    llvm::errs() << "warning: instantiate hierarchy: some module symbols changed after passes\n";
    for (const auto &sym : moduleSymbolsBefore) {
      if (!moduleSymbolsAfter.count(sym))
        llvm::errs() << "  removed_module=" << sym << "\n";
    }
  }

  CompileStatsSummary compileStats = collectCompileStats(*module, static_cast<int64_t>(logicDepthLimit));
  compileStats.fuseCombEnabled = enableFuseComb;
  printCompileStats(compileStats);

  if (!probeManifestPath.empty()) {
    if (failed(emitProbeManifest(*module, probeManifestPath)))
      return 1;
  }

  auto writeSingleOutputStats = [&]() -> LogicalResult {
    if (outputFilename == "-")
      return success();
    llvm::SmallString<256> statsPath(outputFilename);
    statsPath += ".stats.json";
    return writeCompileStatsJson(statsPath, compileStats);
  };

  auto buildProfileSummary = [&]() -> llvm::json::Object {
    llvm::json::Object obj;
    obj["build_profile"] = buildProfileNorm;
    obj["hierarchy_policy"] = hierarchyPolicy.getValue();
    obj["module_count_before"] = static_cast<int64_t>(moduleSymbolsBefore.size());
    obj["module_count_after"] = static_cast<int64_t>(moduleSymbolsAfter.size());
    obj["hierarchy_preserved"] = hierarchyPreserved;
    obj["inline_policy"] = inlineDecision.mode;
    obj["inline_enabled"] = inlineDecision.enableInline;
    obj["inline_threshold_ops"] = static_cast<int64_t>(inlineDecision.thresholdOps);
    obj["inline_reason"] = inlineDecision.reason;
    obj["canonicalize_budget"] = static_cast<int64_t>(effectiveCanonicalizeBudget);
    obj["module_op_count"] = static_cast<int64_t>(moduleOpCount);
    obj["cpp_shard_threshold_lines"] = static_cast<int64_t>(cppShardThresholdLines);
    obj["cpp_shard_threshold_bytes"] = static_cast<int64_t>(cppShardThresholdBytes);
    obj["cpp_shard_max_ast_nodes"] = static_cast<int64_t>(cppShardMaxAstNodes);
    obj["profile_pass_timing"] = collectPassTiming;
    return obj;
  };

  auto writeProfileArtifact = [&](llvm::StringRef lane) -> LogicalResult {
    if (profileJsonPathResolved.empty())
      return success();
    llvm::json::Object root;
    root["version"] = 1;
    root["input"] = inputFilename;
    root["emit_kind"] = emitKind;
    root["lane"] = lane.str();
    root["build_profile"] = buildProfileNorm;
    root["module_op_count"] = static_cast<int64_t>(moduleOpCount);

    llvm::json::Object timing;
    timing["parse_ms"] = static_cast<int64_t>(parseMs);
    timing["passes_ms"] = static_cast<int64_t>(passMs);
    timing["emit_ms"] = static_cast<int64_t>(emitMs);
    timing["total_ms"] = static_cast<int64_t>(
        std::chrono::duration_cast<std::chrono::milliseconds>(Clock::now() - tProgramStart).count());
    root["timing"] = std::move(timing);

    llvm::json::Object memory;
    memory["peak_rss_bytes"] = static_cast<int64_t>(getPeakRssBytes());
    memory["current_rss_bytes"] = static_cast<int64_t>(getCurrentRssBytes());
    root["memory"] = std::move(memory);

    llvm::json::Object inlineObj;
    inlineObj["mode"] = inlineDecision.mode;
    inlineObj["enabled"] = inlineDecision.enableInline;
    inlineObj["threshold_ops"] = static_cast<int64_t>(inlineDecision.thresholdOps);
    inlineObj["reason"] = inlineDecision.reason;
    root["inline"] = std::move(inlineObj);
    llvm::json::Object hierarchyObj;
    hierarchyObj["policy"] = "strict";
    hierarchyObj["module_count_before"] = static_cast<int64_t>(moduleSymbolsBefore.size());
    hierarchyObj["module_count_after"] = static_cast<int64_t>(moduleSymbolsAfter.size());
    hierarchyObj["preserved"] = hierarchyPreserved;
    root["hierarchy"] = std::move(hierarchyObj);
    root["canonicalize_budget"] = static_cast<int64_t>(effectiveCanonicalizeBudget);

    llvm::json::Object stats;
    stats["reg_count"] = compileStats.regCount;
    stats["reg_bits"] = compileStats.regBits;
    stats["mem_count"] = compileStats.memCount;
    stats["mem_bits"] = compileStats.memBits;
    stats["logic_depth_limit"] = compileStats.logicDepthLimit;
    stats["max_logic_depth"] = compileStats.maxLogicDepth;
    stats["wns"] = compileStats.wns;
    stats["tns"] = compileStats.tns;
    stats["fuse_comb_enabled"] = compileStats.fuseCombEnabled;
    root["compile_stats"] = std::move(stats);

    if (passTimingCollector)
      root["passes"] = passTimingCollector->toJson();

    return writeProfileJson(profileJsonPathResolved, root);
  };

  const auto tEmitStart = Clock::now();
  if (emitKind == "none") {
    emitMs = static_cast<uint64_t>(std::chrono::duration_cast<std::chrono::milliseconds>(Clock::now() - tEmitStart).count());
    if (failed(writeProfileArtifact("no-emit")))
      return 1;
    return 0;
  }
  if (!outDir.empty()) {
    std::error_code ec = llvm::sys::fs::create_directories(outDir);
    if (ec) {
      llvm::errs() << "error: cannot create --out-dir " << outDir << ": " << ec.message() << "\n";
      return 1;
    }

    std::string top = topSymbol(*module);
    if (top.empty()) {
      llvm::errs() << "error: cannot determine top symbol (missing pyc.top and no func.func)\n";
      return 1;
    }

    if (emitKind == "verilog") {
      if (cppOnly) {
        llvm::errs() << "error: --emit=verilog is not allowed with --sim-mode=cpp-only\n";
        return 1;
      }
      llvm::json::Array verilogFiles;
      bool targetFpga = (targetKind == "fpga");
      if (!targetFpga && targetKind != "default") {
        llvm::errs() << "error: unknown --target: " << targetKind << " (expected: default|fpga)\n";
        return 1;
      }
      if (includePrims) {
        auto primDir = findPrimitivesDir(argv[0]);
        if (!primDir) {
          llvm::errs() << "error: cannot locate runtime/verilog for primitives; set PYC_PRIMITIVES_DIR\n";
          return 1;
        }
        llvm::SmallString<256> primOut(outDir);
        llvm::sys::path::append(primOut, "pyc_primitives.v");
        if (failed(emitPrimitivesFile(primOut, *primDir, targetFpga)))
          return 1;
        verilogFiles.push_back("pyc_primitives.v");
      }

      pyc::VerilogEmitterOptions opts;
      opts.includePrimitives = false; // out-dir mode uses pyc_primitives.v (or expects external primitives)
      opts.targetFpga = targetFpga;

      for (auto f : module->getOps<func::FuncOp>()) {
        if (f.isDeclaration())
          continue;
        std::string fname = (f.getSymName() + ".v").str();
        llvm::SmallString<256> path(outDir);
        llvm::sys::path::append(path, fname);

        std::error_code fe;
        llvm::raw_fd_ostream os(path, fe, llvm::sys::fs::OF_Text);
        if (fe) {
          llvm::errs() << "error: cannot open " << path << ": " << fe.message() << "\n";
          return 1;
        }
        if (failed(pyc::emitVerilogFunc(*module, f, os, opts)))
          return 1;
        verilogFiles.push_back(fname);
      }

      if (failed(updateManifest(outDir, top, std::move(verilogFiles), /*cppMods=*/std::nullopt)))
        return 1;

      // Optional Yosys stub (sanity synth).
      llvm::SmallString<256> ysPath(outDir);
      llvm::sys::path::append(ysPath, "yosys_synth.ys");
      std::string ys;
      llvm::raw_string_ostream yss(ys);
      yss << "# Generated by pycc\n";
      if (includePrims)
        yss << "read_verilog -sv pyc_primitives.v\n";
      for (auto f : module->getOps<func::FuncOp>()) {
        if (f.isDeclaration())
          continue;
        yss << "read_verilog -sv " << f.getSymName().str() << ".v\n";
      }
      yss << "hierarchy -top " << top << "\n";
      yss << "proc; opt; memory; opt\n";
      yss << "synth -top " << top << "\n";
      yss.flush();
      if (failed(writeFile(ysPath, ys)))
        return 1;

      llvm::SmallString<256> statsPath(outDir);
      llvm::sys::path::append(statsPath, "compile_stats.json");
      if (failed(writeCompileStatsJson(statsPath, compileStats)))
        return 1;
      emitMs = static_cast<uint64_t>(std::chrono::duration_cast<std::chrono::milliseconds>(Clock::now() - tEmitStart).count());
      if (failed(writeProfileArtifact("out-dir:verilog")))
        return 1;

      return 0;
    }

    if (emitKind == "cpp") {
      llvm::json::Array cppFiles;
      std::vector<CppManifestSource> cppManifestSources;
      pyc::CppEmitterOptions cppEmitOpts;
      if (cppShardMaxAstNodes > 0) {
        cppEmitOpts.evalTopoChunkNodes = cppShardMaxAstNodes;
        cppEmitOpts.combChunkNodes = cppShardMaxAstNodes;
      }
      cppEmitOpts.probePlanPath = probePlanPath;

      // Collect direct dependencies per module for header includes.
      llvm::StringMap<llvm::SmallVector<std::string>> deps;
      for (auto f : module->getOps<func::FuncOp>()) {
        if (f.isDeclaration())
          continue;
        auto &v = deps[f.getSymName()];
        f.walk([&](pyc::InstanceOp inst) {
          auto calleeAttr = inst->getAttrOfType<FlatSymbolRefAttr>("callee");
          if (!calleeAttr)
            return;
          v.push_back(calleeAttr.getValue().str());
        });
        std::sort(v.begin(), v.end());
        v.erase(std::unique(v.begin(), v.end()), v.end());
      }

      bool splitModule = false;
      if (cppSplitMode == "module")
        splitModule = true;
      else if (cppSplitMode == "none")
        splitModule = false;
      else {
        llvm::errs() << "error: unknown --cpp-split mode: " << cppSplitMode << " (expected: module|none)\n";
        return 1;
      }

      auto writeHeaderPreamble = [&](llvm::raw_ostream &os, llvm::StringRef moduleName) {
        (void)moduleName;
        os << "// pyCircuit C++ emission (split)\n";
        os << "#pragma once\n";
        os << "#include <cstdlib>\n";
        os << "#include <cstdint>\n";
        os << "#include <fstream>\n";
        os << "#include <iostream>\n";
        os << "#include <memory>\n";
        os << "#include <string>\n";
        os << "#include <cpp/pyc_sim.hpp>\n";
      };

      auto writeSourcePreamble = [&](llvm::raw_ostream &os, llvm::StringRef headerName) {
        os << "// pyCircuit C++ emission (split)\n";
        os << "#include \"" << headerName << "\"\n\n";
        os << "namespace pyc::gen {\n\n";
      };

      auto writeSourceEpilogue = [&](llvm::raw_ostream &os) { os << "} // namespace pyc::gen\n"; };

      for (auto f : module->getOps<func::FuncOp>()) {
        if (f.isDeclaration())
          continue;
        std::string moduleName = f.getSymName().str();
        const bool probeOnlyModule = isProbeOnlyFunc(f);
        std::string headerName = moduleName + ".hpp";
        llvm::SmallString<256> headerPath(outDir);
        llvm::sys::path::append(headerPath, headerName);

        std::string emitted;
        {
          llvm::raw_string_ostream emitOs(emitted);
          if (failed(pyc::emitCppFunc(*module, f, emitOs, cppEmitOpts)))
            return 1;
          emitOs.flush();
        }

        if (splitModule) {
          std::string splitErr;
          auto split = splitEmittedModule(emitted, moduleName, splitErr);
          if (!split) {
            llvm::errs() << "error: failed to split module `" << moduleName << "`: " << splitErr << "\n";
            return 1;
          }

          std::error_code fe;
          llvm::raw_fd_ostream hos(headerPath, fe, llvm::sys::fs::OF_Text);
          if (fe) {
            llvm::errs() << "error: cannot open " << headerPath << ": " << fe.message() << "\n";
            return 1;
          }
          writeHeaderPreamble(hos, moduleName);
          for (const std::string &dep : deps[f.getSymName()])
            hos << "#include \"" << dep << ".hpp\"\n";
          hos << "\nnamespace pyc::gen {\n\n";
          hos << split->headerBody << "\n";
          hos << "} // namespace pyc::gen\n";

          auto writeSourceFile = [&](llvm::StringRef fileName,
                                     llvm::StringRef kind,
                                     llvm::StringRef shard,
                                     llvm::ArrayRef<const SplitMethodDef *> defs) -> LogicalResult {
            if (defs.empty())
              return success();
            llvm::SmallString<256> srcPath(outDir);
            llvm::sys::path::append(srcPath, fileName);
            std::error_code se;
            llvm::raw_fd_ostream sos(srcPath, se, llvm::sys::fs::OF_Text);
            if (se) {
              llvm::errs() << "error: cannot open " << srcPath << ": " << se.message() << "\n";
              return failure();
            }
            writeSourcePreamble(sos, headerName);
            for (const SplitMethodDef *m : defs)
              sos << m->definition << "\n";
            writeSourceEpilogue(sos);

            CppManifestSource info;
            info.path = fileName.str();
            info.module = moduleName;
            info.shard = shard.str();
            info.kind = kind.str();
            info.probeOnly = probeOnlyModule;
            auto srcOrErr = llvm::MemoryBuffer::getFile(srcPath);
            if (!srcOrErr) {
              llvm::errs() << "error: cannot read generated source " << srcPath << " for manifest metadata\n";
              return failure();
            }
            llvm::StringRef srcText = (*srcOrErr)->getBuffer();
            info.bytes = static_cast<uint64_t>(srcText.size());
            info.lines = static_cast<uint64_t>(countLines(srcText));
            info.sourceHash = md5HexOfString(srcText);
            info.complexityScore = computeComplexityScore(info.lines, info.bytes, info.kind);
            info.predictedCompileCost = computePredictedCompileCost(info.complexityScore, info.kind);
            cppManifestSources.push_back(std::move(info));
            return success();
          };

          std::vector<const SplitMethodDef *> allMethods;
          std::vector<const SplitMethodDef *> coreMethods;
          std::vector<const SplitMethodDef *> evalMethods;
          std::vector<const SplitMethodDef *> tickMethods;
          std::vector<const SplitMethodDef *> combMethods;
          size_t totalBytes = 0;
          size_t totalLines = 0;
          allMethods.reserve(split->methods.size());
          for (const auto &m : split->methods) {
            const SplitMethodDef *pm = &m;
            allMethods.push_back(pm);
            totalBytes += m.bytes;
            totalLines += m.lines;
            if (m.kind == "comb")
              combMethods.push_back(pm);
            else if (m.kind == "eval")
              evalMethods.push_back(pm);
            else if (m.kind == "tick")
              tickMethods.push_back(pm);
            else
              coreMethods.push_back(pm);
          }

          bool oversized =
              (totalLines > static_cast<size_t>(cppShardThresholdLines)) ||
              (totalBytes > static_cast<size_t>(cppShardThresholdBytes));

          if (!oversized) {
            if (failed(writeSourceFile(moduleName + ".cpp", "module", "000", allMethods)))
              return 1;
          } else {
            bool wroteAny = false;
            if (failed(writeSourceFile(moduleName + "__core.cpp", "core", "core", coreMethods)))
              return 1;
            if (!coreMethods.empty())
              wroteAny = true;
            auto shardPredictedCost = [&](size_t lines, size_t bytes, llvm::StringRef kind) -> double {
              return computePredictedCompileCost(
                  computeComplexityScore(static_cast<uint64_t>(lines), static_cast<uint64_t>(bytes), kind), kind);
            };

            constexpr double kShardSoftMaxSourcePredictedCompileCost =
                kHardMaxSourcePredictedCompileCost * 0.90;

            auto writeMaybeShardedMethods = [&](llvm::StringRef stem,
                                                llvm::StringRef kind,
                                                llvm::ArrayRef<const SplitMethodDef *> defs,
                                                bool allowSharding) -> LogicalResult {
              if (defs.empty())
                return success();

              size_t totalBytesLocal = 0;
              size_t totalLinesLocal = 0;
              for (const SplitMethodDef *m : defs) {
                totalBytesLocal += m->bytes;
                totalLinesLocal += m->lines;
              }
              double totalPredictedCostLocal = shardPredictedCost(totalLinesLocal, totalBytesLocal, kind);
              bool needsSharding =
                  allowSharding && defs.size() > 1 &&
                  ((totalBytesLocal > static_cast<size_t>(cppShardThresholdBytes)) ||
                   (totalLinesLocal > static_cast<size_t>(cppShardThresholdLines)) ||
                   (totalPredictedCostLocal > kShardSoftMaxSourcePredictedCompileCost));

              if (!needsSharding) {
                if (failed(writeSourceFile(moduleName + "__" + stem.str() + ".cpp", kind, stem, defs)))
                  return failure();
                wroteAny = true;
                return success();
              }

              size_t shardIdx = 0;
              std::vector<const SplitMethodDef *> shardDefs;
              size_t shardBytes = 0;
              size_t shardLines = 0;
              auto flushShard = [&]() -> LogicalResult {
                if (shardDefs.empty())
                  return success();
                char shardNum[8];
                std::snprintf(shardNum, sizeof(shardNum), "%03zu", shardIdx++);
                if (failed(writeSourceFile(moduleName + "__" + stem.str() + "_" + shardNum + ".cpp",
                                           kind, shardNum, shardDefs)))
                  return failure();
                wroteAny = true;
                shardDefs.clear();
                shardBytes = 0;
                shardLines = 0;
                return success();
              };

              for (const SplitMethodDef *m : defs) {
                double projectedCost =
                    shardPredictedCost(shardLines + m->lines, shardBytes + m->bytes, kind);
                bool limitHit =
                    !shardDefs.empty() &&
                    ((shardBytes + m->bytes > static_cast<size_t>(cppShardThresholdBytes)) ||
                     (shardLines + m->lines > static_cast<size_t>(cppShardThresholdLines)) ||
                     (projectedCost > kShardSoftMaxSourcePredictedCompileCost));
                if (limitHit) {
                  if (failed(flushShard()))
                    return failure();
                }
                shardDefs.push_back(m);
                shardBytes += m->bytes;
                shardLines += m->lines;
              }
              return flushShard();
            };

            // tick methods can be enormous for large top-level modules (e.g. JanusBccBackendCompat).
            // Allow sharding to avoid compiler instability/timeouts on a single huge TU.
            if (failed(writeMaybeShardedMethods("tick", "tick", tickMethods, /*allowSharding=*/true)))
              return 1;

            if (failed(writeMaybeShardedMethods("eval", "eval", evalMethods, /*allowSharding=*/true)))
              return 1;
            if (failed(writeMaybeShardedMethods("comb", "comb", combMethods, /*allowSharding=*/true)))
              return 1;

            if (!wroteAny) {
              if (failed(writeSourceFile(moduleName + "__core.cpp", "core", "core", allMethods)))
                return 1;
            }
          }
        } else {
          std::error_code fe;
          llvm::raw_fd_ostream hos(headerPath, fe, llvm::sys::fs::OF_Text);
          if (fe) {
            llvm::errs() << "error: cannot open " << headerPath << ": " << fe.message() << "\n";
            return 1;
          }
          hos << "// pyCircuit C++ emission (prototype)\n";
          hos << "#pragma once\n";
          hos << "#include <cstdlib>\n";
          hos << "#include <iostream>\n";
          hos << "#include <memory>\n";
          hos << "#include <cpp/pyc_sim.hpp>\n";
          for (const std::string &dep : deps[f.getSymName()])
            hos << "#include \"" << dep << ".hpp\"\n";
          hos << "\nnamespace pyc::gen {\n\n";
          hos << emitted;
          hos << "\n} // namespace pyc::gen\n";
        }

        cppFiles.push_back(headerName);
      }

      if (splitModule) {
        if (failed(enforceCppCompileBudgets(*module, cppManifestSources)))
          return 1;
        llvm::SmallString<256> manifestPathStorage;
        if (!cppManifestPath.empty()) {
          manifestPathStorage = cppManifestPath;
        } else {
          manifestPathStorage = outDir;
          llvm::sys::path::append(manifestPathStorage, "cpp_compile_manifest.json");
        }
        std::vector<std::string> includeDirs = {std::string(outDir)};
        std::vector<std::string> compileDefines;
        std::string topHeaderName = top + ".hpp";
        llvm::json::Object manifestProfile = buildProfileSummary();
        manifestProfile["pycc_peak_rss_bytes"] = static_cast<int64_t>(getPeakRssBytes());
        manifestProfile["pass_time_ms"] = static_cast<int64_t>(passMs);
        auto toolchainRoot = findToolchainRoot(argv[0]);
        if (failed(writeCppCompileManifest(manifestPathStorage, top, cppManifestSources,
                                           includeDirs, compileDefines, toolchainRoot, topHeaderName,
                                           manifestProfile)))
          return 1;
      }

      if (failed(updateManifest(outDir, top, /*verilogMods=*/std::nullopt, std::move(cppFiles))))
        return 1;

      llvm::SmallString<256> statsPath(outDir);
      llvm::sys::path::append(statsPath, "compile_stats.json");
      if (failed(writeCompileStatsJson(statsPath, compileStats)))
        return 1;
      emitMs = static_cast<uint64_t>(std::chrono::duration_cast<std::chrono::milliseconds>(Clock::now() - tEmitStart).count());
      if (failed(writeProfileArtifact("out-dir:cpp")))
        return 1;

      return 0;
    }

    llvm::errs() << "error: unknown --emit kind: " << emitKind << "\n";
    return 1;
  }

  std::error_code ec;
  llvm::raw_fd_ostream os(outputFilename, ec, llvm::sys::fs::OF_Text);
  if (ec) {
    llvm::errs() << "error: cannot open " << outputFilename << ": " << ec.message() << "\n";
    return 1;
  }

  if (emitKind == "verilog") {
    if (cppOnly) {
      llvm::errs() << "error: --emit=verilog is not allowed with --sim-mode=cpp-only\n";
      return 1;
    }
    pyc::VerilogEmitterOptions opts;
    opts.includePrimitives = includePrims;
    if (targetKind == "fpga")
      opts.targetFpga = true;
    else if (targetKind != "default") {
      llvm::errs() << "error: unknown --target: " << targetKind << " (expected: default|fpga)\n";
      return 1;
    }
    if (failed(pyc::emitVerilog(*module, os, opts)))
      return 1;
    if (failed(writeSingleOutputStats()))
      return 1;
    emitMs = static_cast<uint64_t>(std::chrono::duration_cast<std::chrono::milliseconds>(Clock::now() - tEmitStart).count());
    if (failed(writeProfileArtifact("single:verilog")))
      return 1;
    return 0;
  }
  if (emitKind == "cpp") {
    pyc::CppEmitterOptions cppEmitOpts;
    if (cppShardMaxAstNodes > 0) {
      cppEmitOpts.evalTopoChunkNodes = cppShardMaxAstNodes;
      cppEmitOpts.combChunkNodes = cppShardMaxAstNodes;
    }
    cppEmitOpts.probePlanPath = probePlanPath;
    if (failed(pyc::emitCpp(*module, os, cppEmitOpts)))
      return 1;
    if (failed(writeSingleOutputStats()))
      return 1;
    emitMs = static_cast<uint64_t>(std::chrono::duration_cast<std::chrono::milliseconds>(Clock::now() - tEmitStart).count());
    if (failed(writeProfileArtifact("single:cpp")))
      return 1;
    return 0;
  }

  llvm::errs() << "error: unknown --emit kind: " << emitKind << "\n";
  return 1;
}
