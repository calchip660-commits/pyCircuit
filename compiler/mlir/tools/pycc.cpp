static std::string trimCopy(llvm::StringRef s) {
  size_t b = 0;
  size_t e = s.size();
  while (b < e && std::isspace(static_cast<unsigned char>(s[b])))
    ++b;
  while (e > b && std::isspace(static_cast<unsigned char>(s[e - 1])))
    --e;
  return s.substr(b, e - b).str();
}

static std::string normalizeWhitespace(llvm::StringRef s) {
  std::string out;
  out.reserve(s.size());
  bool prevSpace = false;
  for (char c : s) {
    if (std::isspace(static_cast<unsigned char>(c))) {
      if (!prevSpace) {
        out.push_back(' ');
        prevSpace = true;
      }
    } else {
      out.push_back(c);
      prevSpace = false;
    }
  }
  return trimCopy(out);
}

static size_t countLines(llvm::StringRef s) {
  if (s.empty())
    return 0;
  size_t n = 1;
  for (char c : s)
    if (c == '\n')
      ++n;
  return n;
}

static uint64_t getPeakRssBytes() {
  struct rusage ru {};
  if (::getrusage(RUSAGE_SELF, &ru) != 0)
    return 0;
#if defined(__APPLE__)
  return static_cast<uint64_t>(ru.ru_maxrss);
#else
  return static_cast<uint64_t>(ru.ru_maxrss) * 1024ull;
#endif
}

static uint64_t getCurrentRssBytes() {
#if defined(__APPLE__)
  mach_task_basic_info info {};
  mach_msg_type_number_t count = MACH_TASK_BASIC_INFO_COUNT;
  if (task_info(mach_task_self(), MACH_TASK_BASIC_INFO, reinterpret_cast<task_info_t>(&info), &count) != KERN_SUCCESS)
    return 0;
  return static_cast<uint64_t>(info.resident_size);
#elif defined(__linux__)
  std::ifstream in("/proc/self/statm");
  long totalPages = 0;
  long rssPages = 0;
  if (!(in >> totalPages >> rssPages))
    return 0;
  long pageSize = ::sysconf(_SC_PAGESIZE);
  if (pageSize <= 0)
    return 0;
  return static_cast<uint64_t>(rssPages) * static_cast<uint64_t>(pageSize);
#else
  return getPeakRssBytes();
#endif
}

static std::string md5HexOfString(llvm::StringRef s) {
  llvm::MD5 md5;
  md5.update(s);
  llvm::MD5::MD5Result res;
  md5.final(res);
  llvm::SmallString<32> hex;
  llvm::MD5::stringifyResult(res, hex);
  return hex.str().str();
}

static uint64_t computeComplexityScore(uint64_t lines, uint64_t bytes, llvm::StringRef kind) {
  uint64_t score = lines + (bytes / 64ull);
  if (kind == "tick")
    score = (score * 150ull) / 100ull;
  else if (kind == "eval")
    score = (score * 130ull) / 100ull;
  else if (kind == "comb")
    score = (score * 120ull) / 100ull;
  return score;
}

static double computePredictedCompileCost(uint64_t complexityScore, llvm::StringRef kind) {
  double w = 1.0;
  if (kind == "tick")
    w = 1.45;
  else if (kind == "eval")
    w = 1.25;
  else if (kind == "comb")
    w = 1.15;
  return static_cast<double>(complexityScore) * w;
}

