static std::optional<std::string> findToolchainRoot(const char *argv0) {
  auto tryRoot = [&](llvm::SmallString<256> root) -> std::optional<std::string> {
    llvm::SmallString<256> runtimeLib(root);
    llvm::sys::path::append(runtimeLib, "lib", runtimeLibBasename());
    llvm::SmallString<256> cmakeCfg(root);
    llvm::sys::path::append(cmakeCfg, "share", "pycircuit", "cmake", "pycircuitConfig.cmake");
    if (llvm::sys::fs::exists(runtimeLib) || llvm::sys::fs::exists(cmakeCfg))
      return root.str().str();
    return std::nullopt;
  };

  if (const char *env = std::getenv("PYC_TOOLCHAIN_ROOT")) {
    llvm::SmallString<256> root(env);
    if (auto found = tryRoot(root))
      return found;
  }

  llvm::ErrorOr<std::string> resolved = llvm::sys::findProgramByName(argv0 ? argv0 : "");
  llvm::SmallString<256> exePath;
  if (resolved) {
    exePath = *resolved;
  } else if (argv0 && *argv0) {
    exePath = argv0;
  }
  if (exePath.empty())
    return std::nullopt;

  llvm::SmallString<256> realExe;
  if (llvm::sys::fs::real_path(exePath, realExe))
    realExe = exePath;
  llvm::SmallString<256> parent = llvm::sys::path::parent_path(realExe);
  if (llvm::sys::path::filename(parent) == "bin") {
    llvm::SmallString<256> root = llvm::sys::path::parent_path(parent);
    if (auto found = tryRoot(root))
      return found;
  }

  llvm::SmallString<256> cur = parent;
  for (unsigned i = 0; i < 6 && !cur.empty(); ++i) {
    if (auto found = tryRoot(cur))
      return found;
    cur = llvm::sys::path::parent_path(cur);
  }
  return std::nullopt;
}

static LogicalResult emitPrimitivesFile(llvm::StringRef outPath, llvm::StringRef primDir, bool targetFpga) {
  static const char *kFiles[] = {
      "pyc_reg.v",
      "pyc_fifo.v",
      "pyc_byte_mem.v",
      "pyc_sync_mem.v",
      "pyc_sync_mem_dp.v",
      "pyc_async_fifo.v",
      "pyc_cdc_sync.v",
  };

  std::string buf;
  llvm::raw_string_ostream ss(buf);
  ss << "// pyCircuit Verilog primitives (concatenated)\n";
  ss << "/* verilator lint_off DECLFILENAME */\n\n";
  if (targetFpga)
    ss << "`define PYC_TARGET_FPGA 1\n\n";
  for (const char *name : kFiles) {
    llvm::SmallString<256> path(primDir);
    llvm::sys::path::append(path, name);
    auto fileOrErr = llvm::MemoryBuffer::getFile(path);
    if (!fileOrErr) {
      llvm::errs() << "error: cannot read primitive file: " << path << "\n";
      return failure();
    }
    ss << "// --- " << name << "\n";
    ss << fileOrErr->get()->getBuffer() << "\n\n";
  }
  ss << "/* verilator lint_on DECLFILENAME */\n";
  ss.flush();
  return writeFile(outPath, buf);
}

static LogicalResult updateManifest(llvm::StringRef outDirPath, llvm::StringRef top,
                                   std::optional<llvm::json::Array> verilogMods,
                                   std::optional<llvm::json::Array> cppMods) {
  llvm::SmallString<256> path(outDirPath);
  llvm::sys::path::append(path, "manifest.json");

  llvm::json::Object manifest;
  if (llvm::sys::fs::exists(path)) {
    if (auto mb = llvm::MemoryBuffer::getFile(path)) {
      if (auto parsed = llvm::json::parse(mb->get()->getBuffer())) {
        if (auto *obj = parsed->getAsObject())
          manifest = *obj;
      }
    }
  }

  manifest["top"] = top.str();
  if (!manifest.get("verilog_modules"))
    manifest["verilog_modules"] = llvm::json::Array();
  if (!manifest.get("cpp_modules"))
    manifest["cpp_modules"] = llvm::json::Array();
  if (verilogMods)
    manifest["verilog_modules"] = std::move(*verilogMods);
  if (cppMods)
    manifest["cpp_modules"] = std::move(*cppMods);

  std::string buf;
  llvm::raw_string_ostream ss(buf);
  llvm::json::OStream j(ss, 2);
  llvm::json::Value v(std::move(manifest));
  j.value(v);
  ss << "\n";
  ss.flush();
  return writeFile(path, buf);
}

