static FailureOr<llvm::json::Object> parseTbPayload(ModuleOp module) {
  auto payloadOpt = getTbPayloadAttr(module);
  if (!payloadOpt)
    return failure();
  auto parsed = llvm::json::parse(*payloadOpt);
  if (!parsed || !parsed->getAsObject()) {
    module.emitError("[PYC920] invalid `pyc.tb.payload` JSON encoding");
    return failure();
  }
  return *parsed->getAsObject();
}

static LogicalResult emitTbTextFromPayload(ModuleOp module, llvm::StringRef kind, llvm::raw_ostream &os) {
  auto payloadOr = parseTbPayload(module);
  if (failed(payloadOr))
    return failure();
  auto payload = *payloadOr;
  if (kind == "cpp") {
    if (auto txt = payload.getString("cpp_text")) {
      os << txt->str();
      return success();
    }
    module.emitError("[PYC921] testbench payload missing `cpp_text`");
    return failure();
  }
  if (kind == "verilog") {
    if (auto txt = payload.getString("sv_text")) {
      os << txt->str();
      return success();
    }
    module.emitError("[PYC922] testbench payload missing `sv_text`");
    return failure();
  }
  module.emitError("[PYC923] unsupported testbench emit kind `") << kind << "`";
  return failure();
}

static std::optional<std::string> findPrimitivesDir(const char *argv0) {
  // Allow explicit override (useful for installed toolchains).
  if (const char *env = std::getenv("PYC_PRIMITIVES_DIR")) {
    llvm::SmallString<256> cand(env);
    llvm::sys::path::append(cand, "pyc_reg.v");
    if (llvm::sys::fs::exists(cand))
      return std::string(env);
  }

  auto tryDir = [&](llvm::SmallString<256> dir) -> std::optional<std::string> {
    llvm::SmallString<256> probe(dir);
    llvm::sys::path::append(probe, "pyc_reg.v");
    if (llvm::sys::fs::exists(probe))
      return dir.str().str();
    return std::nullopt;
  };

  auto tryRoot = [&](llvm::StringRef root) -> std::optional<std::string> {
    llvm::SmallString<256> dir(root);
    llvm::sys::path::append(dir, "runtime", "verilog");
    if (auto d = tryDir(dir))
      return d;

    dir = root;
    llvm::sys::path::append(dir, "include", "verilog");
    if (auto d = tryDir(dir))
      return d;

    dir = root;
    llvm::sys::path::append(dir, "include", "pyc", "verilog");
    if (auto d = tryDir(dir))
      return d;
    return std::nullopt;
  };

  // Current working directory.
  llvm::SmallString<256> cwd;
  if (!llvm::sys::fs::current_path(cwd)) {
    if (auto d = tryRoot(cwd))
      return d;
  }

  // Walk up from the executable path (common for in-tree builds).
  llvm::SmallString<256> exe(argv0 ? argv0 : "");
  if (!exe.empty()) {
    llvm::SmallString<256> rp;
    if (!llvm::sys::fs::real_path(exe, rp)) {
      llvm::SmallString<256> cur = llvm::sys::path::parent_path(rp);
      for (unsigned i = 0; i < 6 && !cur.empty(); ++i) {
        if (auto d = tryRoot(cur))
          return d;
        cur = llvm::sys::path::parent_path(cur);
      }
    }
  }

  return std::nullopt;
}

